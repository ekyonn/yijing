5. L'attente, Su : Arrêt forcé, empêchement, obstacle, danger. Résistance aux
forces nuisibles.
TEXTE I
L’homme droit et ferme aura un succès brillant, s’affermira, se
complétera heureusement ; il saura traverser les difficultés. (Litt. : Le
grand fleuve — les grandes eaux ; moyen de briser les obstacles, etc.)
SYMBOLISME
Com. I. Su est : droiture en face du péril, fermeté inébranlable ne se
laissant ni abattre ni surprendre, et dont la justice ne s’affaiblit, ne
s’épuise jamais. Celui qui occupe par sa dignité la place du ciel doit
maintenir la justice, la fidélité au devoir ; il pourra alors triompher des
difficultés, quelque chose qu’il entreprenne.
Com. II. Su est formé du trigramme « nuage » au-dessus du
trigramme « ciel ». Ce sont les nuages s’élevant dans le ciel ; ainsi le
sage goûte joie et plaisir.
<l>
La ligne 1 devient yin -
Obstacle, danger dans un pays désert, éloigné. Avec de l’adresse, de la
persévérance et de la fermeté, on en sortira sans dommage. [(Le sage) ne
veut pas briser violemment les difficultés ; il suit les principes sans faillir.
Com. II.]
<l>
La ligne 2 devient yin -
Obstacle, danger dans un récif, sur un banc de sable, une île qui obstrue la
voie ; avec un peu de peine, on peut en sortir. (Siao yeu yuen indique
généralement les méchants propos.)
<l>
La ligne 3 devient yin -
Arrêt, danger dans les terrains marécageux, fondrières. Si des voleurs
surviennent, ils saisiront (les gens ainsi arrêtés). Danger extérieur ; avec
prudence et circonspection on ne périra pas.
<l>
La ligne 4 passe au yang -
Danger, dans le sang, en sortant d’une caverne (d’être tué par des
brigands).
<l>
La ligne 5 devient yin -
Danger en des festins ; issue heureuse si l’on est modéré. Les banquets
seront d’heureux effet si l’on y garde le milieu. [Medium tenuere beati ;
nécessité de la modération dans les plaisirs. Com. II.]
<l>
La ligne 6 passe au yang -
Danger que court celui qui est entré dans une caverne. S’il survient des
hommes inattendus, trois même, et qu’on les traite avec respect, le bonheur
s’ensuivra. [Bien que la situation ne convienne pas, il n’y aura pas grande
faute. Com. II.]
Note. — Ce paragraphe est interpolé : la première phrase équivaut à § 4. La seconde,
« s’il survient, etc. » est du symbolisme postérieur, inspiré par la vue des trois lignes
pleines des trigrammes inférieurs. Ce sont là les trois hôtes.
