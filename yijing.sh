11. Paix, T’ai : union, pénétration ; libéralité, générosité.
TEXTE I
Le petit va au grand ; le grand va au petit ; de là heureux
développement des choses. (La matière va à la force ; la terre au ciel ;
les petits vont vers les grands.)
SYMBOLISME
T’ai exprime les rapports, la pénétration réciproque du ciel et de la
terre. Le prince, par sa puissance, complète les actes réguliers du ciel
et de la terre ; aidant, assistant leurs convenances, il soutient en
même temps le peuple.
COMMENTAIRE I
1. Quand cette pénétration réciproque s’opère, le ciel et la terre
s’harmonisent et tous les êtres se produisent. Le haut et le bas
s’harmonisent et leurs intentions et fins sont identiques.
2. Le trigramme inférieur représente le principe actif et le trigramme
supérieur, le principe passif. Le premier figure la puissance constitutive
et le grand ; le second représente la soumission, la réceptivité et le
petit, l’homme vulgaire.
La voie du grand s’élève ; celle du petit est pleine de tristesse et
silencieuse.
<l>
La ligne 1 devient yin -
(Image de l’union.) Les plantes dont les racines s’entremêlent ne peuvent
être arrachées l’une sans l’autre (ainsi l’union multiplie les forces).
<l>
La ligne 2 devient yin -
Si l’on supporte la rudesse des autres (pour rester unis) et que, s’étant
éloigné (litt. : ayant passé le fleuve, hō), on n’oublie pas ceux qui sont restés
en arrière, que l’on traite amicalement ces égaux (pih) ; grâce à cette union,
on se maintiendra glorieusement dans la voie du milieu.
<l>
La ligne 3 devient yin -
Sans surface plane, point de déclivité ; sans allée, point de retour. Les
difficultés dont on se garde n’ont point d’effet funeste et ne donnent point lieu
de regretter une conduite droite ; la félicité s’ensuivra. [La première phrase
représente les rapports du ciel et de la terre. Com. II.]
<l>
La ligne 4 passe au yang -
Celui qui est flottant et peu ferme dans sa droiture ne gagnera pas ses
voisins par ses richesses et ne deviendra pas digne de confiance, par cela seul
qu’il aura été averti (union empêchée par des défauts).
<l>
La ligne 5 passe au yang -
Ti-Yi, mariant sa jeune sœur, assura la prospérité, un succès supérieur
(l’union des époux). [Il assure la réalisation de ses désirs, en restant juste.
Com. II.]
Note : Ti-Yi est donné comme l’avant-dernier souverain des Shang-Yin. Il prescrivit
que les princesses impériales, mariées à des grands de moindre rang, perdraient leur
qualité princière. Il assura ainsi leur soumission à leurs époux et la bonne harmonie du
ménage.
<l>
La ligne 6 passe au yang -
Les murs d’une ville tomberont dans la boue, si elle n’a pas un chef (qui
maintienne l’union) : elle se fait alors elle-même son sort et n’aura que honte
à attendre. [Ainsi l’ordre qui doit y régner, se trouble. Com. II.]
