13. La communion, T’ong : union, harmonie.
TEXTE I
Si les hommes vivent en concorde dans un pays, il prospérera ; on y
traversera les grandes difficultés. Le sage atteindra aisément sa
perfection.
SYMBOLISME
La bonté, obtenant les dignités, gardant le milieu et s’accordant avec
le ciel, forme le T’ong yin. Le feu sous le ciel forme l’hexagramme. Par
leur vertu, le sage connaît la nature des choses et en saisit le tout
harmonique (Shan y erh tchi t’ong). Le feu éclaire le ciel, le fait
connaître.
COMMENTAIRE I
Nous voyons ici l’effet de l’action du ciel. La force mêlée d’habileté et
de perspicacité, observant la justice. Ainsi l’homme supérieur est bon
et juste, et pénètre la pensée de tout ce qui est sous le ciel. Ainsi
règne l’union.
<l>
La ligne 1 devient yin -
Unis, à la maison, les hommes ne commettent pas de faute. [S’ils en
sortent même (restant tels), ils n’encourront pas de blâme. Com. II.] (Effets
de l’union domestique.)
<l>
La ligne 2 passe au yang -
L’union d’hommes par cabale est chose très funeste et dont on aura à se
repentir. [C’est la voie du regret. Com. II.]
<l>
La ligne 3 devient yin -
(Moyen de garder l’union.) Cacher ses armes tout en restant vigilant (sur
son élévation) et ne point les relever de longtemps (litt. : trois ans). [Agir
toujours pacifiquement. Com. II.]
<l>
La ligne 4 devient yin -
Bien que monté dans une forteresse, le bon prince ne cherche pas à
dominer par la force (la violence et les attaques) ; son gouvernement sera
heureux. [Dans les difficultés mêmes, il retourne à la justice. Il se retire en sa
forteresse quand le droit ne règne pas. Com. II.]
<l>
La ligne 5 devient yin -
L’union fait succéder la joie aux pleurs. Par elle l’armée du prince a
triomphé (elle rentre triomphante), on va à sa rencontre, les peuples se
soumettent. L’homme de concorde met au-dessus de tout la modération et la
justice. [Ses adversaires mêmes reviennent à lui en esprit de paix. Com. II.]
<l>
La ligne 6 devient yin -
L’union s’étendant dans les parties même les plus éloignées du pays
dissipera toute peine, toute cause de regret.
