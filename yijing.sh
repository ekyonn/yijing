<l>
La ligne 5 passe au yang -
C’est au sage d’écarter (les maux) et de résoudre (les difficultés) ; s’il y
réussit, il gagnera la confiance du vulgaire.
<l>
Si le prince est assez habile pour atteindre d’une flèche un faucon posé sur
le haut d’un mur élevé, il aura le succès et saura disperser les rebelles (3 e
sens, Com. II).
