14. Grandes possessions, Tá : grand, grandeur, grandir, développement.
TEXTE I
Grandeur, commencement développé.
SYMBOLISME
Le Koua représente le feu au-dessus du ciel. Ainsi l’homme supérieur
réprime le mal, répand le bien et se conforme en tout au décret du
ciel.
COMMENTAIRE I
La vraie grandeur, c’est la bonté possédant les honneurs ; le grand
gardant le milieu, et petits et grands restant en harmonie. Force et
fermeté avec habileté et intelligence forment sa vertu. Il est d’accord
avec le ciel, agit à son temps et prospère grandement.
<l>
La ligne 1 devient yin -
L’homme vraiment grand n’a pas de rapport avec les méchants et, bien
qu’il rencontre des difficultés, il ne commettra pas de faute.
<l>
La ligne 2 devient yin -
(Grandeur matérielle, ses avantages.) Ce qu’on transporte dans un grand
(tá) chariot, quelque part qu’on aille, arrivera sûrement. [Ce qu’on y entasse
ne subira pas de dommage. Com. II.]
<l>
La ligne 3 devient yin -
La grandeur est le partage des seuls Kongs. Eux seuls peuvent faire des
dons convenables au Fils du Ciel. Un homme inférieur ne le peut pas.
L’homme vulgaire blesserait (le Fils du Ciel s’il s’avisait de lui faire un don).
<l>
La ligne 4 devient yin -
L’homme grand ne commet pas de faute concernant ce qui ne lui
appartient pas [n’est pas son droit. Com.]. [Il le distingue clairement. Com.
II.]
<l>
La ligne 5 passe au yang -
Le grand apporte toute sincérité dans ses rapports ; majestueux et grave,
il est heureux. Il exprime sincèrement ses pensées. [Il est cru en ce qu’il dit ;
même quand il change d’avis on a confiance en sa sagesse. Com. II.]
<l>
La ligne 6 devient yin -
Il reçoit le secours du ciel. Tout tourne à son avantage.
<h>
15. Modestie, K’ien : respect, condescendance, bienveillance, modestie.
TEXTE I
Si cette vertu grandit en lui, le sage aura un heureux sort.
SYMBOLISME
L’hexagramme représente la terre ayant sous elle une montagne. Ainsi
l’homme supérieur diminue l’abondant et augmente ce qui est en
indigence et, arrangeant proportionnellement toutes choses, il établit
la paix et répand ses faveurs.
COMMENTAIRE I
La règle du ciel est de répandre en bas ses bienfaits et de faire briller
la lumière ; celle de la terre est de porter son activité de bas en haut.
La voie du ciel est de dépouiller l’arrogant et de combler l’humble de
biens. Celle de la terre est de renverser la fortune du superbe et de
faire surabonder l’humble. Les Esprits abattent l’orgueilleux et
favorisent l’humble. La voie de l’homme est de haïr le superbe et
d’aimer l’humble. La modestie honorée brille ; abaissée, elle ne
prévarique point. C’est la fin suprême du sage.

<l>
La ligne 1 passe au yang -
Là où l’on respecte le sage, on sortira heureusement des difficultés. [On
doit se maintenir par l’abaissement de soi-même. Com. II.]
<l>
La ligne 2 passe au yang -
Le respect qui se manifeste au-dehors est du plus heureux effet [quand il
est établi au milieu du cœur. Com. II.].
<l>
La ligne 3 devient yin -
Le sage diligent et respectueux sera heureux jusqu’à la fin. [Tous se
soumettront à lui. Com. II.]
<l>
La ligne 4 passe au yang -
Il est toujours avantageux d’agrandir son respect, sa modestie et de ne
point passer la mesure de la modération.
<l>
La ligne 5 passe au yang -
Même sans richesse on sera aimé et aidé de ses concitoyens, si l’on est
respectueux et bienveillant. Mais, à l’égard des insoumis, il est bon d’user de
la contrainte et des armes même.
<l>
La ligne 6 passe au yang -
Si la bonté n’est pas comprise, on doit alors mettre ses troupes en
mouvement et châtier les villes et les États. [Si la bonté ne réussit pas. Com.
II.]
