améliorera le pays ; la confiance s’étendra jusqu’aux porcs et aux
poissons, on traversera les difficultés comme un fleuve que l’on passe
monté sur un vaste vaisseau de bois. Le milieu ferme donne l’avancement 
et l’achèvement, et fait correspondre aux vues du ciel.
<l>
La ligne 1 devient yin -
Une circonspection ferme est heureuse ; une conduite différente n’amènera
pas de sujet de joie. [Si ces dispositions changent. Com. II.] Il faut une paix
intérieure vigilante, autrement pas de joie.
<l>
La ligne 2 devient yin -
La grue appelle du lieu de sa retraite et ses petits lui répondent dans un
