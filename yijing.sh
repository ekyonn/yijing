COMMENTAIRE I
Si en avançant le petit agit selon les circonstances et qu’il garde le
juste milieu, les petites affaires qu’il fera réussiront. Le fort qui perd sa
dignité et ne garde pas le milieu ne peut plus, par cela même, rien
faire de grand. Il est représenté ici sous la figure de l’oiseau volant ; le
bruit de son vol ne peut s’élever, mais uniquement diminuer. — Le
grand résiste, le petit cède.
