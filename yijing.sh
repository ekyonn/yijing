31. Influence, attraction, sensibilité. K’an et hiēn : 1. Réunir, accorder, ensemble ; 2. Mouvoir, émouvoir le
cœur, exciter le sentiment (excitation physique et morale) ; être en
mouvement, remuer ; 3. Harmonie, droiture, sincérité.
TEXTE I
Le sentiment d’union, d’affection, conduit à prendre femme.
SYMBOLISME
Un marais au-dessus d’une montagne ; ainsi l’homme supérieur
s’abaisse pour accueillir tout le monde (comme l’eau sur la montagne).
COMMENTAIRE I
Hiēn a le même sens que k’an (2 e sens). Le fort en dessus, le faible en
dessous (selon la figure), leurs influences s’unissent et s’harmonisent.
C’est la stabilité et la satisfaction. L’homme s’inclinant vers la femme,
c’est la condition d’un mariage prospère. Le ciel et la terre mettent
tout en mouvement et tout se forme, naît. Le Saint meut le cœur des
hommes et le monde alors connaît la paix et la concorde. En voyant
ces divers mouvements, on peut comprendre les sentiments du ciel et
de la terre.
TEXTE II
Note. Nous avons ici une série d’expressions consacrées et familières, dont l’idée de
remuer forme la base commune. Le § 4 se rapporte seul à hiēn, concorde, et le § 3 à
« réunir en se mouvant ». Le Com. I parle des mouvements généraux de la nature
d’après l’ontologie et la morale chinoises. Les §§ 1 à 3 signalent des manières d’agir et
de se tenir contraires aux règles de la bienséance, de ces rites qui formaient la base
de l’organisation sociale et de la morale en Chine.
<l>
La ligne 1 passe au yang -
Prendre et mouvoir le gros orteil ; s’abaisser par condescendance (Com.
II). [Figure signifiant aussi se mouvoir pour prendre quelque chose placé plus
bas. Com.]
<l>
La ligne 2 passe au yang -
Remuer les chairs en marchant est une mauvaise contenance. (I. e.
marcher avec tant de hâte et de trouble que la chair de la jambe se remue.
Mouvements contraires au juste milieu et à la gravité.)
<l>
La ligne 3 devient yin -
Marcher en réunissant les jambes et se serrant contre la personne que l’on
suit est une manière blâmable. (On ne pourra se garer contre les coups.
Com.) [Ce qui en résulte est l’abaissement. Com. II.] Cf. Siao-Hio, livre II, §
156, prescrivant de marcher un peu en arrière d’une personne honorable.
<l>
La ligne 4 devient yin -
La droiture ferme est cause de succès (sens 3) et éloigne tout regret. Une
conduite douteuse éloigne tout le monde ; les amis à peine restent attachés.
— [Tout est bien tant qu’on ne se laisse pas porter au mal ; la conduite
douteuse est sans grandeur. Com. II.]
<l>
La ligne 5 devient yin -
« Être ému jusqu’en la chair des épaules », cela indique des pensées
généreuses et vives. (Com. Tchong-tao, II e , f. 3.) — Dicton figurant une
émotion profonde.
<l>
La ligne 6 passe au yang -
« Remuer les mâchoires et la langue » signifie parler trop. [Cela désigne
une bouche avide de paroles. Com. II.]
<h>
32. Constance, Hāng : 1. Constance, permanence, stabilité ; 2. Partout,
complètement ; 3. Lune presque pleine.
TEXTE I
La vertu ferme, se développant sans défaillance, arrive à sa perfection
et procure de l’avantage en tout et partout.
SYMBOLISME
Tonnerre et vent forment le Koua. Le sage reste ferme et ne change
pas de conduite.
COMMENTAIRE I
Hāng indique constance, permanence. C’est le fort en haut, le faible en
bas (voir le Koua), le tonnerre et le vent en communication et
concorde. C’est le moteur et le mû docile, la constance heureuse et la
stabilité dans sa voie. La voie, les procédés du ciel et de la terre se
perpétuent et ne cessent jamais. Le succès gît dans la persévérance
en ce qu’on a commencé.
Le soleil et la lune, participant au ciel, perpétuent leur éclat. Les
quatre saisons changeant et se succédant peuvent se perpétuer
parfaitement. Le saint persévérant en sa voie, le monde se transforme
et se perfectionne. En cette perpétuité, on peut voir les sentiments, les
tendances du ciel et de la terre et de toutes choses.
<l>
La ligne 1 passe au yang -
Celui qui fait des efforts extrêmes pour rendre sa position ferme réussira
difficilement. C’est celui qui fait des efforts immodérés et prétentieux.
<l>
La ligne 2 devient yin -
En éloignant toute faute, on s’assure la stabilité (et non par des efforts).
Ou : la constance éloigne toute faute. — Ceci n’est qu’un composé de termes
auguraux pour faire nombre.
<l>
La ligne 3 devient yin -
Celui qui ne rend pas sa vertu constante perpétuera sa honte et ne parfera
que ses erreurs.
<l>
La ligne 4 devient yin -
(Un homme sans vertu est) un terrain sans gibier.
<l>
La ligne 5 passe au yang -
On doit perpétuer ses vertus ; mais à chacun les siennes, celles du mari ne
sont pas celles de l’épouse. [La femme sert un homme et c’est tout jusqu’à sa
mort. L’homme décide du droit. Servir une femme ne lui convient pas. Com.]
<l>
La ligne 6 passe au yang -
Une excitation continuelle (hāng) est chose funeste. Chercher la stabilité
par ses efforts est une erreur. [Elle dépend d’en haut (du ciel. Ces efforts
seront sans résultat ni mérite. Com. II.] Ceci est une répétition du § 1. Il
s’agit de la position d’un homme dans le monde, de son lot en ce monde. Cela
dépend du ciel et non des efforts des hommes.
<h>
33. Retraite, T’ún : I. Retraite, obscurité ; 2. Retirer.
TEXTE I
La vie retirée a des avantages ; le petit s’y affermit et se perfectionne.
SYMBOLISME
Le ciel sur une montagne. Le sage s’éloigne des gens vulgaires non par
haine, mais par dignité.
COMMENTAIRE I
Id. Le fort, dans une position convenable, agit conformément au
temps. Le petit (dans l’obscurité) avance et grandit. Grande est
l’importance de la retraite.
<l>
La ligne 1 passe au yang -
Retirer sa queue uniquement (se tenir en arrière) en présence du danger
sert peu quoi qu’on fasse, il faut se retirer en un lieu obscur et tranquille. [Si
l’on ne fait rien, il n’y aura pas de danger. Com. II.]
Note. « Retirer sa queue » est une expression analogue à celle que nous employons en
faisant allusion à l’autruche : « Mettre sa tête sous son aile. »
<l>
La ligne 2 passe au yang -
Si pour retenir une chose, on emploie une lanière de cuir de bœuf,
personne ne pourra l’arracher. [On sera ferme en son propos. Com. II.] Si
l’on se garde et retient bien, on réussira.
<l>
La ligne 3 devient yin -
Pour celui qui vit retiré et a une maladie dangereuse, il est bon d’entretenir
des serviteurs ou une épouse secondaire (pour le soigner).
<l>
La ligne 4 devient yin -
Aimer la retraite est bon aux yeux du sage, niais point pour l’homme
vulgaire (qui cherche à paraître). [Le sage l’aime, le vulgaire point. Com. II.]
<l>
La ligne 5 devient yin -
Se plaire dans l’obscurité, c’est un gage de bonheur. [Car alors on peut
diriger toutes ses pensées vers le bien. Com. II.]
<l>
La ligne 6 devient yin -
La retraite dans l’abondance procure tous les biens. Point de doute.
