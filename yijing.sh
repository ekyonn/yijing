12. l'Arrêt, P’ī : 1. Opposition, fermé ; 2. Nuisance, méchanceté ; 3. Blâmer ; 4.
Exclamation ayant un sens négatif.
TEXTE I
Le méchant ne réussit pas, le sage prospère ; le grand va, le petit
vient à lui. (Exemples d’opposition.)
SYMBOLISME
L’obstacle aux relations du ciel et de la terre forme p’ī. En ce cas, le
sage restreint son action et évite les difficultés. Les sages ne tirent pas
d’honneur de leurs émoluments ; ils retiennent leurs vertus en leur
cœur.
La position des trigrammes figure la voie du petit s’élevant ; celle du
grand s’abaissant.
COMMENTAIRE I
P’ī désigne le méchant. Quand p’ī domine, le ciel et la terre sont sans
rapport, les êtres ne se produisent pas. Quand le haut et le bas sont
désunis, l’empire est sans gouvernement digne de ce nom.
<l>
La ligne 1 passe au yang -
Les plantes dont les racines sont entremêlées (résistent et) ne peuvent
être arrachées l’une sans l’autre. (Voir Koua 11, texte II, I ). [Ceci concerne
les princes. Com. II.]
<l>
La ligne 2 passe au yang -
Si par la patience et la constance l’homme vulgaire même réussit, l’homme
supérieur ne le pourrait-il pas ? [Il ne troublera pas le peuple. Com. II.]
<l>
La ligne 3 devient yang -
La patience fait avancer. [Mais il ne convient pas que l’on avance en
dignité, uniquement parce qu’on supporte patiemment les oppositions et
méchancetés. Com. II.]
<l>
La ligne 4 devient yin -
Quand on a pour soi le décret du ciel, on réussira certainement ; les
champs mis en ordre prospéreront (malgré les oppositions, etc). [Les désirs
s’accompliront. Com. II.]
Note : Ces deux paragraphes 3 et 4 sont la suite de 2 et n’ont été séparés que pour
avoir le nombre 6.
<l>
La ligne 5 devient yin -
Heū-p’ī. Faisant cesser les oppositions méchantes, l’homme supérieur
prospérera, ses amis seront comme entourés de branches luxuriantes de
laurier. Le bonheur de l’homme supérieur est dans une fonction exercée avec
justice et convenance.
<l>
La ligne 6 devient yin -
Kīng-p’ī. Détruire, renverser les obstacles et nuisances. On fait succéder
ainsi la joie, la satisfaction. Avant peine, après plaisir (dicton populaire). [Si
les obstacles cessent, étant abattus comment pourraient-ils durer ? Com. II.]
Note. Nous avons ici un exemple d’obstacles, § 1 ; le moyen de les vaincre, §§ 2, 3, 4,
et trois expressions dans lesquelles entre le mot p’ī, 2, 5, 6.
