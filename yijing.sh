35. Progrès, Ts’in : avancer, croître, grandir.
TEXTE I
T’sin, croissance de la nature, joie et vigueur comme celle d’un prince
se servant de chevaux donnés, maître d’une population toujours
croissante et recevant chaque jour de nombreux dons. (Trois fois par
jour donnant audience et recevant des dons.) — (Prospérité croissante.)
SYMBOLISME
C’est le soleil s’élevant au-dessus de la terre (le matin). Ainsi le sage
fait briller ses vertus d’un grand éclat.
COMMENTAIRE I
Ts’in est avancer, progresser. C’est la lumière s’élevant au-dessus de
la terre ; la soumission à la grandeur éclatante ; le faible, bon,
avançant, s’élevant. C’est ainsi que le prince prospérant reçoit des
dons de chevaux, etc.
<l>
La ligne 1 passe au yang -
Que l’on avance, grandisse ou diminue, si l’on est droit on sera heureux. Si
l’on n’a pas encore mérité une confiance absolue, que l’on soit ferme et juste,
et il n’arrivera rien de fâcheux. [Que l’homme reste seul, ferme et juste,
même sans obtenir de fonction. Com. II.]
<l>
La ligne 2 passe au yang -
Qu’on ait succès ou chagrin, la droiture ferme donnera le bonheur ; on
recevra une large prospérité de ses aïeux. (Les ancêtres veillent sur leurs
descendants et les protègent.)
<l>
La ligne 3 devient yang -
Quand tout le monde vous applaudira, se fiera à vous, vous serez à l’abri
de tout reproche. (Suite au § 2.) [Si l’on suit la voie générale (Com.),
l’avantage en sera une conduite noble. Com. II.]
<l>
La ligne 4 devient yin -
Avancer comme un rat (plein de cupidité et de crainte) est chose dont on
doit se garder. [C’est quand on cherche une position qui ne convient pas.
Com. II.]
<l>
La ligne 5 passe au yang -
Quand on est à l’abri de tout reproche (par sa bonne conduite), on ne doit
point se préoccuper de perte ou de profit. On avancera heureusement, on ne
sera point sans avantage. [On sera loué de tous. Com. II.]
<l>
La ligne 6 devient yin -
On ne doit avancer ses cornes (employer la force) que pour châtier une
ville coupable. Il est bien alors de s’exposer au danger. Mais le succès lui-
même n’est pas sans regret. (On doit être triste d’être obligé de sévir.) [Ces
actes ne sont pas éclatants. Com. II.] (On ne doit pas en tirer gloire, mais les
faire avec peine.)
