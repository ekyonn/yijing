6. Le Conflit, Song : Recours au prince, procès, affaires publiques.
TEXTE I
L’homme droit les empêchera ; même en y procédant avec crainte et
prudence, si le milieu en est heureux, la fin en sera funeste. On pourra
aborder les grands, mais on ne traversera pas les difficultés.
SYMBOLISME
I. Force au-dessus, péril au-dessous (pour le petit qui plaide)
représentent les procès.
II. Le ciel (au-dessus), l’eau (au-dessous) forment le koua (d’où
danger). Le sage, entreprenant une affaire délibère mûrement sur son
commencement (avant de commencer).
<l>
La ligne 1 passe au yang -
Si l’on se désiste (d’un procès) entrepris, bien qu’il y ait quelque bruit (à ce
sujet), le résultat final sera heureux. [Si le procès ne se poursuit pas malgré
les pourparlers, l’affaire s’éclaircira. Com. II. ]
<l>
La ligne 2 devient yin -
Si celui qui se désiste d’un procès (ou l’abandonne) retourne chez lui et
cache l’affaire aux gens de son endroit, il n’en aura pas de désagréments. [Si
le petit conteste contre le grand, les maux viendront comme pris à la main. Com. II. ]
<l>
La ligne 3 devient yang -
a. Si l’on entretient en soi les vertus des anciens, on prospérera ; la fin sera
heureuse.
b. S’appliquer aux affaires du prince ne suffit pas pour faire l’homme parfait.
[L’un et l’autre sont choses heureuses (a et b). Com. II.]
<l>
La ligne 4 devient yin -
Celui qui succombe dans un procès doit s’amender, retourner à la justice et
reprendre son calme ; alors il s’affermira heureusement.
<l>
La ligne 5 devient yin -
Lutter et plaider sa cause apporte une suprême fortune. (*)
D’un procès le commencement (seul) est heureux. Il conduit aux querelles,
haines, etc.
<l>
La ligne 6 devient yin -
Bien qu’on réussisse dans les affaires publiques (qu’on l’emporte en un
procès) et qu’on obtienne à la Cour une distinction honorifique, celle-ci pourra
être enlevée jusqu’à trois fois (ou : avant le troisième jour terminé). [Le
succès final n’est jamais assuré. Com.]
