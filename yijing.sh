39. Obstruction, Kién : Difficulté, danger, noble hardiesse.
TEXTE I
Énergie dans les difficultés ; peut réussir d’un côté et pas de l’autre.
Par son succès se montre le grand homme. Sa perfection est heureuse
et peut sauver des périls.
SYMBOLISME
L’eau sur une montagne. Par la vertu de l’énergie, l’homme sage se
corrige et perfectionne sa vertu.
COMMENTAIRE I
Kién est difficulté, danger se présentant en face ; si, voyant le danger,
on sait rester ferme, quelle grande sagesse ! Le succès ou l’insuccès
vient de ce que l’on garde la voie droite ou que la sagesse est à bout.
On reconnaît à cela l’homme vraiment grand et plein de mérites. Il
faut rester ferme dans la justice et la bonté pour établir fermement un
État. Oh ! que le moment des difficultés a d’importance !
<l>
La ligne 1 passe au yang -
Si l’on va (courageusement) aux choses difficiles, on reviendra comblé de
louanges. [Il convient d’être ferme. Com. II.]
<l>
La ligne 2 passe au yang -
Si le prince et ses ministres ont difficultés sur difficultés, ce n’est point
(nécessairement) parce qu’ils recherchent leur propre avantage (ou : cela ne
dépend pas nécessairement d’eux-mêmes).
<l>
La ligne 3 devient yin -
L’un va aux difficultés et revient après au repos. Ou : qui va aux difficultés
(courageusement) revient aux profits (en obtient). Dicton.
<l>
La ligne 4 passe au yang -
Un autre y va et revient uni à ceux qui les ont partagées. [Il faut être fidèle
dans ses fonctions. Com. II.] C’est dans les difficultés que les amis se
montrent.
<l>
La ligne 5 devient yin -
Un autre y va également et revient plus éclairé et intelligent. [Il a la juste
mesure. Com. II.]
<l>
La ligne 6 passe au yang -
Un dernier y va de même et revient plein de mérite et de succès. Il atteint
les qualités du grand homme.
