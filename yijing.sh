text="
1. Le Créateur, K’ien : principe actif, force vitale universelle.
TEXTE I
C’est l’origine, le progrès, l’affermissement et l’achèvement des
êtres.
SYMBOLISME
Ciel en action, puissance suprême. Le sage met toute sa force en
action et ne se repose jamais.
COMMENTAIRE I
Grand est vraiment le principe originaire, le K’ien ! Toutes choses
en proviennent. Il est l’origine du Ciel et le comprend tout entier. Les
nuages s’y produisent et la pluie s’en répand, tous les différents êtres
en tirent leurs formes. Pour expliquer l’origine et la fin de tout, on
emploie six lignes en six positions convenables (les koua). Par leur
moyen, comme par un char attelé de six dragons, on parcourt le ciel
(on en sonde les mystères). L’action régulière du K’ien est de former
et de transformer les êtres. Chacun a sa nature et son emploi fixés ;
ainsi il maintient l’union et l’harmonie suprêmes. Ainsi toute chose
progresse, reçoit ses bienfaits et se perfectionne.
Les chefs s’élèvent au-dessus de tous et, de la sorte, les États ont
paix et prospérité. Le ciel se meut et agit avec grande puissance. Ainsi
le grand, le sage exerce son activité sans jamais se reposer
complètement.
<l>
La ligne 1 devient yin -
Le dragon caché, enfoncé dans l’abîme, est sans action.
<l>
La ligne 2 devient yin -
Mais il apparaît, on le voit dans les champs. Les bienfaits font voir l’homme
vraiment supérieur. [L’influence de la vertu se répand au loin avantageusement. Commentaire II.]
<l>
La ligne 3 devient yin -
Le sage est ainsi actif et vigilant tout le jour ; le soir même, il est attentif,
veillant et ne se repose pas. S’il survient un danger, une difficulté, il n’en
éprouvera aucune suite fâcheuse. [Le sage rétablit toujours la voie de la
justice, la foule et refoule sans cesse. Commentaire II.]
<l>
La ligne 4 devient yin -
Le dragon se remue (saute) dans l’abîme ; c’est bien. (Le prince sage
exerce son action sur le monde.) [Il se répand au-dehors et avance.
Commentaire II.]
<l>
La ligne 5 devient yin -
Il s’élève volant dans le ciel. Le principe actif s’élève et produit dans le ciel.
[Ainsi le grand homme s’élève à la hauteur de sa mission et de ses œuvres.
Commentaire II.]
<l>
La ligne 6 devient yin -
S’il devient trop fort et dominant, il y aura lieu de le regretter. [Trop de
bonne fortune ne peut durer si l’on n’est prudent et modéré en tout.
Commentaire II.] Le principe actif doit céder au principe réceptif à certains
moments ou bien les êtres ne se reproduiront pas.
<l>
Toutes les lignes vont yin -
Voir de nombreux dragons sans tête, c’est bonheur. [Lorsque le grand, le
fort, sait être doux et complaisant, c’est un dragon sans tête ; c’est là une
condition de succès et de bonheur. La hauteur, l’obstination, l’arrogance,
représentées par la tête, sont des sources de grands maux. Commentaire.]
[Les propriétés du ciel ne doivent pas toujours prédominer. Commentaire III.]
(et étouffer toute influence de la terre ou de l’homme. La prospérité du
monde dépend de la combinaison des forces de ces trois puissances) : ceci
est une interpolation.
<h>
2. Le réceptif, Kwūn : a) Principe passif, réceptif. 1. Terre étendue et portant ; 2.
Femme, femelle ; 3. Soumission, complaisance ; 4. Support, appui.
— b) Kwun, fermer, lier. c) Kwun : vêtement impérial.
TEXTE I
Terre recevant l’action du principe actif et portant, produisant ;
femelle portant et produisant, concourant à la série des quatre actes
de la vie des êtres. Le grand, en ce qu’il fait, ne doit point prévenir
l’action des forces naturelles ; s’il le fait, il échouera ; s’il agit après et
la suit, il réussira. S’il met son propre avantage au-dessus de tout, il
pourra acquérir des amis d’un côté, mais il les perdra de l’autre. S’il
cherche la paix, la concorde de toutes les puissances, il réussira et
sera heureux.
SYMBOLISME
Le double trigramme de la terre, la terre partout, indique la vertu
caractéristique de celle-ci : supporter, entretenir tout. Le sage s’y
conformant, entretient et contient tous les êtres par sa vaste vertu.
COMMENTAIRE I
Suprême est le principe passif de la terre, tous les êtres en reçoivent
leur naissance, mais il est complètement soumis au principe actif du
ciel. La terre en son amplitude porte toutes choses ; sa vertu est sans
limite comme (celle du ciel). Contenant immense, son éclat est grand.
Tous les êtres s’y développent ensemble. L’animal femelle (la jument)
a la nature de la terre ; elle la parcourt sans terme ni fin. Douceur et
soumission (qui sont ses qualités) prouvent le bonheur ; le sage doit
les pratiquer. Le bien résultant de la fermeté paisible et durable
correspond à l’immense vertu de la terre. (La jument est passive et
active comme la terre qui reçoit et produit.)
<l>
La ligne 1 passe au yang -
Quand on marche sur le verglas, c’est que la forte gelée arrive à son point
extrême. [Cette phrase est une expression consacrée, peignant l’arrivée de
l’hiver et le commencement des gelées. D’après la cosmologie chinoise, c’est
le moment où le principe passif, réceptif, entre en action, commence à
dominer et produit le fort hiver qui est son triomphe. — « Marcher sur le
verglas » signifie aussi marcher avec prudence. — Le principe passif va ainsi
du verglas à la glace. La forte gelée commençant par un fin verglas est aussi
le type du principe producteur terrestre qui commence par l’atome pour
produire les plus grands corps. Nous avons donc en cette phrase la plus forte
expression du principe Kwūn, objet de la section.]
[Lorsque le principe passif commence la période de froid, suivant son cours
naturel, il en vient aux fortes gelées. Com. II.]
<l>
La ligne 2 passe au yang -
Droite, carrée, vaste (la terre), d’elle-même et sans travail, procure
toujours des biens à l’homme. [La voie de la terre est brillante, produit de
grandes choses. Com. II.]
<l>
La ligne 3 devient yang -
Celui qui maintient les lois (du ciel et de la terre) peut espérer le succès. [Il
brillera à l’occasion. Com. II.]
Quand on s’applique au service du prince, si on ne le fait pas parfaitement, on
devra le quitter. [Savoir se conduire est glorieux et grand. Com. II.]
[Bien qu’on ne le fasse pas d’abord parfaitement, on pourra réussir à la fin.
Com.]
<l>
La ligne 4 passe au yang -
(Sens kwun, fermé.) Sac lié, fermé, ne peut être loué ou blâmé (quant à
son contenu). [La prudence évite toute injure, tout dommage. Com. II.] (Le
sac fermé est la terre, dont le contenu est inconnu et caché. C’est aussi
l’image de la prudence nécessaire à celui qui sert le prince.)
<l>
La ligne 5 passe au yang -
Kwun, vêtement impérial. Ou « vêtement de dessous jaune ». Figure de la
terre qui est jaune et sous le ciel. [La beauté, le bel arrangement est en elle.
Com. II.] [et se montre au-dehors. Com.] Le texte ajoute : Honneur, bonheur
suprême.
<l>
La ligne 6 passe au yang -
Le dragon combat dans l’espace ; son sang est noirâtre et jaunâtre.
(Reprise du koua précédent : le principe actif lutte dans l’immensité pour
produire ; il produit le ciel qui est noirâtre et la terre qui est jaunâtre, selon la
terminologie chinoise.)
<l>
Toutes les lignes deviennent yang -
Le nombre six est un nombre heureux ; s’en servir assure le succès.
(Phrase interpolée, expliquant l’usage des six lignes de chaque figure.)
