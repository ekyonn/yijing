3. Difficulté initiale, T’un et tchun : A. 1. Bourgeon, pousse ; 2. Croissance, activité ; 3.
Grandir, avancer. — B. Difficultés, arrêté dans son avancement, échec.
TEXTE I
C’est le commencement se développant, mais non affermi, achevé ;
arrêté au contraire. Par l’activité, le succès s’établit heureusement en
tout ce qu’on peut faire.
SYMBOLISME
Le tonnerre sous les nuages, entravé par eux, figure les difficultés. Le
sage arrange les choses selon les exigences de celles-ci. [Com. II.]
COMMENTAIRE I
(Les deux premiers koua ont représenté séparément les deux
principes.) Ici commencent leur mélange et leurs difficultés. C’est
l’activité dans ces difficultés qui mène au grand succès. Quand le Ciel
envoie (un temps de) troubles et (de) ténèbres, il est bon d’établir des
chefs féodaux et de ne point se livrer à la sécurité et au repos. (Ceci
semble ajouté par Wuh-Wang ou Tchéou-Kong pour justifier leur
conduite. Wuh-Wang créa plus de cent fiefs.)
<l>
La ligne 1 devient yin -
Pour s’établir solidement, il faut se maintenir en fermeté et droiture. (Pour
maintenir le royaume), il est bon de constituer des chefs féodaux. Bien qu’on
ait des difficultés, la volonté doit toujours s’attacher au devoir. Si, bien
qu’élevé, on condescend aux besoins des petits, on s’attachera fortement le
peuple.
<l>
La ligne 2 passe au yang -
Tchun est comme arrêté par les difficultés, comme un cavalier dont le
cheval veut reculer (ou : un cheval monté qui veut...).
C’est comme la jeune fille qui veut épouser un brigand ravisseur, elle doit
rester inébranlable et attendre (plutôt) dix ans. (Malgré cela), elle pourra
alors se marier et être mère. (Il est ici question de difficultés.) [Un équipage
solide, l’enfantement après dix ans indique le retour à la prospérité, à
l’avancement. Com. II.]
<l>
La ligne 3 devient yang -
Lorsque le gibier poursuivi s’enfonce inopinément dans une forêt profonde,
le sage aime mieux l’abandonner que de s’exposer au danger. S’il l’y poursuit,
il aura lieu de s’en repentir. [Il sera réduit à l’extrémité. Com. II.]
<l>
La ligne 4 passe au yang -
Si elle est demandée en mariage selon les rites, qu’elle y aille et ce sera
bien. Cela lui sera avantageux. [Il en est de même de l’équipage arrêté qui
reprend sa course. Com.]
<l>
La ligne 5 devient yin -
Dans le développement de la sève des bourgeons, si elle se répand
modérément, la croissance sera heureuse ; si elle se répand trop, le
croissance sera empêchée. [Il en est ainsi si elle se répand avant d’être
suffisamment riche et forte. Com.] [Com. II : « obstacle au développement :
s’il est petit, l’issue peut être malheureuse ; s’il est grand,          
fin malheureuse. »]
<l>
La ligne 6 passe au yang -
L’avancement, arrêté comme un cheval monté qui recule, fait répandre
des larmes amères et abondantes.
