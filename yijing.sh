7. L'armée, Sze : chef ; troupes, armée ; peuple, foule.
TEXTE I
Le chef expérimenté (âgé) est heureux et ne commet pas de fautes.
SYMBOLISME
« Eau sous terre, eau au milieu de la terre. » Se conformant à cette
image, le grand entretient le peuple (comme l’eau entretient les
produits de la terre) et forme l’éducation de la foule.
COMMENTAIRE I
Sze est « le peuple ». La droiture ferme rend bon. Par elle on peut
rendre le peuple bon ; par elle, on peut exercer le pouvoir souverain.
Agissant ainsi, le fort atteint son but, et s’exposant même au péril
dans des expéditions guerrières, il pourra ravager (conquérir) le
monde. Le peuple le suit et il réussit. Comment en aurait-il du regret ?
<l>
La ligne 1 passe au yang -
Si une armée fait la campagne selon les règles (la justice et l’art militaire),
tout lui réussira. Ou bien : que l’armée se mette en campagne selon les
règles... ou il lui arrivera malheur. Sinon, il arrivera malheur. [Si elle viole les
règles de la justice et de l’art militaire, elle aura un sort malheureux. Com.]
<l>
La ligne 2 devient yin -
Si le roi est au milieu de ses troupes, tout ira bien et il ne se commettra
pas de faute. Le chef doit répéter ses ordres jusqu’à trois fois, pour s’assurer
d’être bien compris.
<l>
La ligne 3 devient yang -
Qu’une armée ait plusieurs chefs, c’est une chose funeste. Les assistants
du chef doivent le suivre et lui obéir sans commettre de faute. [Un
commentaire (Yih pi tchi neng Kiai) explique ceci autrement : l’armée (en
présence des difficultés qui s’opposent à sa marche) doit céder et s’arrêter
pour éviter toute faute.]
<l>
La ligne 4 passe au yang -
Si l’armée se trouve dans un pays à gros gibier, elle fera bien de le chasser
et de le prendre (pour subvenir à sa subsistance). Ou bien : elle fera bien de
prendre les ordres du chef pour ne pas faire de ravages inutiles.
<l>
La ligne 5 passe au yang -
Si le roi ne commande pas en personne, le fils aîné doit conduire l’armée ;
si ce sont les fils cadets qui commandent, comme ils sont plusieurs, il y aura
(division), faute et insuccès.
<l>
La ligne 6 passe au yang -
Un grand prince qui a obtenu le mandat céleste crée heureusement un
puissant État et donne la stabilité à sa dynastie. Un homme ordinaire ne sait
pas y réussir (ne doit pas être employé).
<h>
8. Union, Pî : union, harmonie, rapprochement, aide.
TEXTE I
Union, chose excellente, commencement heureux que l’on doit porter
à sa perfection, sans faillir. Si l’on n’obtient pas la paix, la concorde, il
en adviendra ultérieurement de grands maux.
SYMBOLISME
C’est l’eau par-dessus la terre figurant la concorde. C’est l’eau qui
pénètre la terre et s’y unit sans laisser aucun intervalle. Animés de cet
esprit, les anciens rois consolidaient leurs États et s’attachaient les
princes.
COMMENTAIRE I
Pî est « concorde, secours ; le petit suivant le grand avec 
soumission ». Si, le puissant étant à sa place, la concorde n’en résulte
pas, la loi morale dépérira.
<l>
La ligne 1 passe au yang -
A l’homme sincère, on s’unit aisément. L’homme droit et ami de la
concorde doit être plein de cet esprit comme un vase de terre rempli (de vin
ou de fruits). Il lui en viendra toujours de nouveaux avantages.
<l>
La ligne 2 passe au yang -
Si l’amour de la concorde provient du cœur, elle s’établira heureusement.
[Elle ne faiblira pas de soi-même. Com. II.]
<l>
La ligne 3 devient yang -
S’unir à quelqu’un qui ne le mérite pas est une source de maux. [Il en
résultera du dommage. Com. II.]
<l>
La ligne 4 passe au yang -
Être uni à celui qui nous est supérieur est chose heureuse. [S’unir aux
sages ; suivre les grands est bien. Com.]
<l>
La ligne 5 devient yin -
(Modèle d’une union parfaite). Le roi chasse en trois saisons ; chaque fois,
il permet que le gibier en vue lui échappe.
Note : Le gibier étant concentré en un parc fermé, on ouvre un côté pour qu’un certain
nombre d’animaux puissent échapper aux flèches ; le peuple, sachant cela, les laisse
fuir et le peuple ne l’avertit pas (de sa fuite du parc) parce qu’il sait que le roi le fait
exprès par bonté.
[Cela prouve que le roi a instruit et formé ce peuple parfaitement. Com. II.]
<l>
La ligne 6 passe au yang -
S’unir à ceux qui ne reconnaissent pas leur chef, ne lui sont pas soumis (ou
s’unir sans reconnaître un chef), est chose mauvaise. [Il ne peut en naître
rien de bon. Com. II.]
<h>
9. L'accomplissement par le Petit, Siao tchu : petit entretien ; éducation, correction ; arrêt.
TEXTE I
L’entretien, l’éducation est comme un gros nuage venant sans pluie
des contrées occidentales et contribuant au développement des êtres.
SYMBOLISME
Le vent soufflant au-dessus du ciel. Ainsi le grand et sage élève et fait
briller la vertu.
COMMENTAIRE I
Le peuple est entretenu quand la bonté occupe le pouvoir et que
grands et petits s’y accommodent entre eux. Avec bonté ferme chez
les uns et soumission chez les autres, la puissance atteint son but, les
desseins utiles s’exécutent. La figure du premier texte représente la
prospérité comme le nuage avançant mais non encore répandu
largement.
<l>
La ligne 1 devient yin -
Restaurer sa nature est la loi de la raison ; elle est sans erreur, c’est une
source de bonheur.
<l>
La ligne 2 devient yin -
Exhorter, amener à cette correction est une œuvre excellente ; cela est le
juste milieu qui ne peut faillir de soi-même.
<l>
La ligne 3 devient yin -
Comme un char dont les roues sont détachées (ne peut marcher), ainsi le
mari et l’épouse qui, détournant leurs regards l’un de l’autre, ne vivent pas en
concorde, ne pourront affermir leur maison.
<l>
La ligne 4 passe au yang -
Chez l’homme sincère le sang jaillit (les bons sentiments se font jour). Le
