Montagne sur eau stagnante. Le sage réprime ainsi sa colère et arrête
ses désirs (comme la montagne presse l’eau).
COMMENTAIRE I
Pour cette offrande simple, il y a un temps convenable ; il y a un
temps pour agrandir le faible et diminuer le fort. Diminuer le plein et
remplir le vide doivent se faire conformément aux circonstances.
<l>
