37. La famille, Kiā : famille, maison.
TEXTE I
L’homme lui donne la prospérité, la femme la complète et la consolide.
SYMBOLISME
Vent sortant du feu. Le sage parle selon la réalité et tient une conduite
toujours bien réglée.
COMMENTAIRE I
La femme remplit les fonctions à l’intérieur ; l’homme à l’extérieur.
L’observance des règles dans leurs actes et leurs rapports, c’est le
suprême droit du ciel et de la terre. Le chef de maison a la dignité,
l’autorité suprême ; il est appelé père-mère. Quand le père et le fils,
les frères aînés et les cadets, les époux, sont ce qu’ils doivent être, la
maison est bien réglée et le monde est en ordre parfait.
<l>
La ligne 1 devient yin -
Celui dont la maison est bien protégée n’aura rien à craindre. [C’est celui
dont la volonté ne varie point. Com. II.]
<l>
La ligne 2 passe au yang -
Si l’on n’a pas besoin d’aller au-dehors, mais que les provisions soient
accumulées dans l’intérieur de la maison, elle sera prospère. [Il faut douceur
dans la prospérité. Com. II.]
<l>
La ligne 3 devient yin -
Si les gens de la maison sont graves et attentifs, craintifs et sachant
regretter une erreur, tout ira bien. Si l’épouse et les enfants sont légers et
bavards, la fin ne sera pas heureuse. [Si les uns ne faillissent pas, les autres
manqueront aux lois de la famille. Com. II.]
<l>
La ligne 4 passe au yang -
Pourvoir abondamment sa maison est une assurance de grands succès. [Ce
sera quand la complaisance (des chefs) et la docilité (des subordonnés)
seront en leur lieu. Com. II.]
<l>
La ligne 5 devient yin -
Avoir une maison puissante est pour le roi une source de sécurité (il ne
devra pas craindre, etc.). [C’est quand il y a mutuelle affection entre ses
membres. Com. II.]
<l>
La ligne 6 devient yin -
Droit, juste et plein de majesté, le roi aura une fin heureuse. [La vraie
dignité restaure la nature en son état d’intégrité primitive. Com. II.]
<h>
38. Opposition, Kwèi : 1. Différent, opposé, éloigné, étrange ; 2. Regarder fixement.
TEXTE I
Kwèi, temps mauvais où les petites affaires seules réussissent et les
grandes point.
SYMBOLISME
Feu au-dessus, eau en dessous ; désunion. Ainsi le sage cherche
l’union et la distinction (juste et convenable de ce qui doit être
distingué).
COMMENTAIRE I
Le feu, se mouvant au-dessus et l’eau en dessous, forment
l’opposition ; de même deux sœurs vivant ensemble, mais ne
s’entendant pas. L’attachement joyeux et intelligent fait que le faible
et bon avance, s’élève et atteint le milieu, s’accordant ainsi avec le
fort. Ainsi les petites affaires réussissent. Le ciel et la terre, bien que
séparés, sont unis dans leur action. L’époux et l’épouse le sont
également et doivent n’avoir qu’une seule volonté. Tous ces êtres sont
distincts et leurs opérations identiques. Le principe, l’opportunité de la
distinction sont choses bien graves.
<l>
La ligne 1 devient yin -
Quand (il n’y a pas d’opposition, mais concorde, que) toute cause funeste
disparaît (on revient à l’union comme) un cheval perdu qui revient de lui-
même sans qu’on le cherche. (Com.) Si l’on rencontre un méchant même, il
n’en surviendra rien de fâcheux (on saura se préserver de ses coups ou éviter
son contact corrupteur).
<l>
La ligne 2 devient yin -
Si l’on s’accorde en route avec son chef, tout réussira. Com. II. On ne se
trompe pas de chemin. — Ou : Si l’on rencontre son chef c’est qu’on ne s’est
pas trompé de chemin. Com. Si l’on a un différend et qu’on se cherche et
s’accorde, on se rencontrera sans effet funeste.
<l>
La ligne 3 devient yang -
(Effets de la discorde, des luttes.) On voit un char entraîné par la force,
son bœuf saisi et arraché, son conducteur maltraité et le nez coupé. Mais un
commencement fâcheux peut avoir une bonne fin, si l’union se rétablit.
<l>
La ligne 4 devient yin -
Seul au milieu de la désunion, si le banni rencontre ensuite un honnête
homme, il pourra avoir des rapports pleins de droiture et les difficultés
antérieures n’engendreront plus d’inconvénient (pour ces deux hommes).
<l>
La ligne 5 passe au yang -
Par l’union toute cause de souci disparaît, les parents sont unis. [Litt. : se
mordent la peau. Ce qui indique pénétration, union de sentiments. Com.] Où
qu’on aille, plus de cause de regret. [On est approuvé de tous. Com. II.] Le
grand est sincère envers le petit. Tchéou-Yih, etc. Sens : Quand la désunion a
disparu, les familles sont dans la joie, mangent abondamment.
<l>
La ligne 6 devient yin -
Seul dans la désunion, le banni est comme quelqu’un qui voit un porc
immonde ou un char plein de mauvais esprits. Il tend d’abord son arc pour
tirer, puis le lâche. Si ce n’est point un méchant, un brigand ravisseur et que
le sort lui soit favorable, le bonheur reviendra.
