17. La conformité, Sūi : 1. Respect, soumission, conformité à ce qui doit être ; 2.
Complaisance ; 3. Fidélité au devoir ; 4. Conséquemment, en ce cas.
TEXTE I
Cette vertu fait parcourir heureusement les quatre stades de 
l’existence (voir Koua 1) sans défaillance ni erreur.
Le fort cédant au faible, l’action dans la satisfaction constituent sūi.
SYMBOLISME
Cet hexagramme est formé du tonnerre sous une eau stagnante. Le
sage à l’approche des ténèbres (de la nuit ou de l’orage) rentre chez
lui tranquillement et se tient en repos.
COMMENTAIRE I
Le succès en est grand ; le monde a, par elle, tout en son temps.
L’importance d’agir conformément aux besoins des divers temps est
des plus grandes.
<l>
La ligne 1 devient yin -
Le magistrat, s’il a commis des fautes et les corrige, sera heureux. Dans
ses rapports avec les hommes, au-dehors, il acquerra les mérites par sa belle
conduite.
<l>
La ligne 2 passe au yang -
S’il fréquente les jeunes gens et laisse de côté les gens âgés (et sages), il
n ’ est pas digne qu’on ait des rapports avec lui.
<l>
La ligne 3 devient yang -
S’il ne fréquente pas les jeunes gens, mais les gens âgés, il arrivera au bien, à la position
désirée ; il restera ferme et droit, sa pensée abandonnera toute bassesse.
<l>
La ligne 4 devient yin -
Ce qu’on obtient par complaisance (blâmable) est d’un profit funeste ;
quand on est sincèrement droit et qu’on reste dans la voie de la vérité, on
acquiert de grands mérites. [Quel regret pourrait-on craindre ? Com. II.]
<l>
La ligne 5 devient yin -
La conduite droite, se tenant toujours à ce qui est bon, donnera le succès.
[Il en sera ainsi de l’homme en fonction gardant toujours le milieu. Com. II.]
<l>
La ligne 6 passe au yang -
Quand le lien de l’attachement et de la soumission (chez les sujets) est
fort, le roi peut offrir des sacrifices sur les montagnes de l’ouest. Il peut
communiquer avec les Esprits ; l’union entre les hommes et les Esprits est
alors parfaite. [Les sacrifices sont exaucés. Com.]
