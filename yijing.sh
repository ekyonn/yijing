34. Le grand pouvoir, Tá tchuáng : grande force.
TEXTE I
Elle donne succès et achèvement.
SYMBOLISME
Le tonnerre au-dessus du ciel représente la plus grande force
COMMENTAIRE I
Tá tchuáng est le grand devenu fort, le mouvement dirigé par la
force ; c’est aussi le fort, juste et droit. La rectitude et la grandeur
manifestent les sentiments du ciel et de la terre.
<l>
La ligne 1 devient yin -
Si la force est dans les doigts de pied, on avancera mal, eût-on même
toute volonté. [La force dans les doigts de pied indique son épuisement, celui
de la vertu. Com. II.] [Ou plutôt la force dans les choses inférieures,
accessoires. Com.]
<l>
La ligne 2 devient yin -
La force employée sagement (ts’ong) donne le succès final. [Quand on
garde le milieu. Com. II.] (Encore une expression purement augurale à
supprimer.)
<l>
La ligne 3 devient yin -
L’homme vulgaire emploie la force, le sage ne le fait pas. Le développement, 
l’emploi de la force est dangereux ; il arrive à qui en use comme au bélier 
qui butte contre une clôture et y fait prendre ses cornes enfoncées.
<l>
La ligne 4 devient yin -
Force employée avec succès comme au cas d’une clôture percée par les
cornes d’un bélier sans que celles-ci y restent prises. C’est la force, par
exemple, des essieux d’un grand char. [Il avance malgré tout. Com. II.] Le
texte est altéré comme le prouve le Commentaire. Yong a été répété par erreur.
<l>
La ligne 5 passe au yang -
La perte d’un bélier qui a perdu toute force n’est pas regrettable.
<l>
La ligne 6 passe au yang -
Le bélier butteur qui, voulant frapper une clôture, ne sait plus ni avancer ni
reculer, sans moyen de s’en tirer, trouvera dans cette difficulté même un
moyen de salut. Ne pouvant plus frapper, il ne prendra plus ses cornes dans
un obstacle ; il a péché par manque de précaution, cela lui servira de leçon.
[Le bon côté de l’accident est qu’il ne prolongera pas sa faute. Com. II.] (I. e.
l’expérience rend prudent.)
