51. Choc, Kán : tonnerre, effroi répandu, crainte.
TEXTE I
Le tonnerre approchant répand l’effroi ; les rires, les causeries
s’arrêtent subitement. Le tonnerre terrifie cent lis, mais ne doit pas
arrêter la cuiller du sacrifice aux esprits.
SYMBOLISME
Le tonnerre répété forme l’hexagramme. Le sage, plein de crainte et
de scrupules prudents, observe et corrige.
COMMENTAIRE I
Cet effroi a d’heureux résultats. Les rires et les causeries gardent ainsi
la mesure. Le tonnerre terrifie ce qui est loin comme ce qui est près.
Mais on doit, quand même, garder le temple des ancêtres, les autels
des génies du sol et présider aux sacrifices. (Malgré l’effroi, on ne peut
les interrompre.)
<l>
La ligne 1 devient yin -
Le tonnerre approchant, on est plein d’effroi ; puis, quand il est passé, on
sourit et on parle tous ensemble (on passe de la crainte à la joie).
<l>
La ligne 2 passe au yang -
Quand le tonnerre gronde, le danger arrive, on croit devoir tout
abandonner et se retirer en un lieu sûr et élevé. Qu’on ne s’obstine pas à
rechercher alors ce qu’on a abandonné ; on le retrouvera quelque temps
après. (Quand le danger sera passé.)
<l>
La ligne 3 devient yang -
Le tonnerre émeut, agite ; il pousse à agir justement.
<l>
La ligne 4 devient yin -
Le tonnerre approchant trouble et interdit [avant même l’éclair. Com. II].
<l>
La ligne 5 passe au yang -
Le tonnerre allant et venant répand l’effroi ; mais on doit tâcher de ne pas
abandonner les affaires entreprises. Même dans le danger pressant, on doit
garder le milieu.
<l>
La ligne 6 passe au yang -
Le tonnerre répand l’effroi et fait partout regarder avec trouble et crainte.
S’il atteint et frappe, il causera un grand mal. (Ou : il corrige le mal.) Mais s’il
ne frappe ni tel personnage, ni ses voisins, ce ne sera point un mal et fera
seulement jaser les habitants d’une même maison.
Quand le tonnerre gronde, que l’orage éclate, le sage recherche la cause de
ce fait (et tâche de profiter de l’avertissement céleste).
