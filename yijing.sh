II.]
<l>
La ligne 2 devient yin -
Celui qui est prudent et appelle à temps à son aide, dans une attaque,
n’aura rien à en craindre. Il a pris le bon moyen. Com. II.
<l>
La ligne 3 devient yin -
Celui qui met sa force dans ses mâchoires* aura malheur. Le sage est
