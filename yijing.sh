Laisser ses propres affaires et aller promptement (au devoir) est une
conduite qui sera sans regret ; mais on doit bien considérer comment on doit
ainsi s’abaisser, se réprimer (sùn). [On doit estimer surtout l’union des
volontés. Com. II.]
<l>
La ligne 2 devient yin -
L’utilité générale, la prospérité requiert que l’on châtie le mal sans le
diminuer ou l’augmenter (en appréciation) et en observant la stricte vérité et
la justice. Com. II.
<l>
La ligne 3 devient yang -
Si trois hommes marchent ensemble, qu’un d’eux quitte ses compagnons
et l’homme qui vient seul après eux en trouvera un. (Ainsi il y aura deux
couples d’amis.) [Quand trois hommes sont ensemble, il y a des différends,
des soupçons. Com. II.]
Note. Litt. : diminuer d’un homme, sùn yih zhīn, pour éviter les querelles.
<l>
La ligne 4 passe au yang -
Diminuer le mal chez quelqu’un, c’est précipiter le moment où il sera
joyeux et content.
<l>
La ligne 5 passe au yang -
Augmenter les biens de quelqu’un par de riches présents qu’il ne peut
refuser, ce sera un avantage capital, une bénédiction d’en haut. Com. II.
Note. Litt. : de tortues à vingt écailles, très rares et précieuses. Il s’agit de dons du
roi.
<l>
La ligne 6 devient yin -
Agrandir ainsi sans diminuer est chose excellente en toute manière. Le roi
(en ce faisant) gagnera ainsi des sujets (qui seront comme sans famille)
entièrement dévoués au souverain. [Ainsi il arrivera au comble de ses désirs.
Com. II.]
