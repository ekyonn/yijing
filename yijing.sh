49. Révolution, Koh : 1. Peau, cuir, tanner, écorcher ; 2. Changer, différer.
TEXTE I
Quand (on change) et qu’on devient sincèrement droit, on sera après
cela constamment sans regret.
SYMBOLISME
L’eau sous le feu figure le changement. L’homme supérieur règle ses
calculs (le calendrier) d’après les changements du ciel et annonce les
temps et les saisons. — Dans les marais même il y a du feu.
COMMENTAIRE I
L’eau et le feu, arrêtant mutuellement leur action, figurent koh. Il en
est de même de deux sœurs habitant ensemble et de volontés
différentes, opposées.
Après un certain temps, se montrer sincère, c’est changer, se
corriger ; on inspire alors confiance. Par le talent et l’intelligence, on
acquiert la satisfaction ; par de grands progrès en vertu, on acquiert la
rectitude. Quand on change de cette manière, tous les sujets de regret
disparaissent.
Le ciel et la terre changeant, les quatre saisons s’accomplissent. Tang
et Wuh-Wang changèrent le décret céleste. Ainsi, ils obéirent au ciel et
répondirent au désir des hommes. Grand, en effet, et bien important
est le moment des changements.
Note. Tang est le chef de la dynastie Shang, qui enleva le trône au dernier des Hia (Ire
dynastie), devenu tyran. Wuh-Wang renversa de même le dernier des Shang et lui
enleva le décret céleste qui l’avait fait roi.
<l>
La ligne 1 devient yin -
(1 er sens). Pour lier on emploie une lanière de cuir jaune. [Ainsi lié, on ne
peut se mouvoir. Com. II.]
Note. Le § 1 prend le mot koh dans le sens de cuir ; les autres dans l’acception de
changement, changement de conduite, correction ; changement d’ordres, de 
dispositions chez le sage et le grand ; changement des saisons, le plus important de
tous, dit un commentaire.
<l>
La ligne 2 passe au yang -
Si l’on sait, après quelque temps (le jour fini), changer et se corriger. Tout
ira bien ; on s’épargnera tout regret. [On aura du succès dans ses actions.
Com. II.]
<l>
La ligne 3 devient yin -
Si l’on corrige le mal, qu’on soit ferme dans les difficultés, et qu’on change
de conduite avec mûre délibération, on deviendra juste et ferme.
<l>
La ligne 4 devient yin -
Quand on s’est corrigé, que toute faute a disparu, que la droiture ferme est
rétablie (dans le cœur), on change son destin ; c’est la voie du bonheur.
<l>
La ligne 5 devient yin -
Le grand homme change à propos comme le tigre change les lignes de sa
peau. Sans consulter le sort, on a confiance en lui (dans ses changements
d’ordre). [La beauté de la peau du tigre, comme la sagesse de l’homme
supérieur, devient de plus en plus brillante. Com. II.]
<l>
La ligne 6 passe au yang -
Le sage change ses voies (quand il faut) comme le léopard. L’homme
vulgaire change son visage [pour plaire aux grands ; Com. II, au prince].
Corriger le mal, se tenir ferme dans la droiture, sont choses éminemment
utiles.
<h>
50. Le Chaudron, T’ing : chaudron à trois pieds, vase du sacrifice, sacrifice.
TEXTE I
Le chaudron est un symbole de principe heureux ; de développement
prospère.
SYMBOLISME
Le feu entrant dans le bois figure « cuire ». Le sage garde sa dignité et
maintient son destin céleste.
COMMENTAIRE I
Les saints cuisaient leurs offrandes pour honorer Shang-ti et faisaient
de grands festins pour entretenir les hommes saints et sages.
La docilité et la finesse d’ouïe, la clarté de la vue chez le faible qui
progresse et s’élève, qui garde le milieu, obéit au puissant et s’accorde
avec lui, tout cela donne la prospérité et le succès.
<l>
La ligne 1 passe au yang -
Quand le chaudron est retourné, on peut aisément en faire tomber ce qui
est mauvais [et s’attacher au bien. Com. II.] Une concubine qui a un fils
n’aura point de regret d’avoir été prise comme telle.
Note. Nous avons ici deux proverbes signifiant que « le bien peut venir du
mal ». Le fils de la concubine peut devenir héritier et, en tout cas, assure une
position honorable à sa mère.
<l>
La ligne 2 devient yin -
Chaudron plein de fruits. — Mon chaudron est plein, mais mon commensal
ne peut venir à moi. — Symboles des peines perdues ou d’un cœur vertueux.
<l>
La ligne 3 devient yin -
Si les oreilles du chaudron sont enlevées, son usage est empêché. On ne
peut ni le lever, ni l’emporter. (Com.) — La chair grasse du faisan (qui y
cuisait) ne pourra être mangée. Mais une heureuse circonstance survenant
dissipera les soucis. (Symbole de malheurs réparés.)
<l>
La ligne 4 devient yin -
Si le chaudron a le pied brisé, les aliments préparés pour le prince sont
renversés ; tout l’extérieur en est souillé. (Symbole du manque de fidélité.)
<l>
La ligne 5 passe au yang -
Un chaudron aux oreilles, aux anneaux d’or, représente la prospérité
venant de la fidélité.
<l>
La ligne 6 devient yin -
Un chaudron aux anneaux de jade représente une grande prospérité, pour
qui tout est avantage. Elle arrive quand le fort et le faible observent la
mesure.
