<l>
La ligne 5 passe au yang -
Le fils qui fait état des soucis de son père méritera des louanges [par ses
vertus. Com. II.]
<l>
La ligne 6 devient yin -
Quand on ne veut pas servir le souverain ni les princes, c’est qu’on a trop
de souci de ses propres affaires et qu’on ne veut s’occuper que d’elles. Les
