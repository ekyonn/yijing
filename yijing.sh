C’est le vent sous la montagne. Le fort au-dessus, le petit au-
dessous ; soumission chez l’un, droiture ferme chez l’autre ; c’est ce
que représente le Koua kū. Aussi le grand doit encourager le peuple et
développer les vertus.
COMMENTAIRE I
Si l’on délibère sagement, le monde sera bien gouverné. Quand on va
à une affaire, on doit délibérer trois jours avant et trois jours après.
<l>
La ligne 1 passe au yang -
Fils qui prend en considération les soucis (kū) de son père et y pense, ne
