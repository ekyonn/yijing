transforme, en le perfectionnant, tout ce qui existe ici-bas.
<l>
La ligne 1 devient yin -
Lorsque la conduite est fautive, mais qu’on cherche avec soin à la rectifier,
on évitera tout blâme. [Si l’on cherche à éviter toute faute. Com. II.]
<l>
La ligne 2 passe au yang -
Le plus bel éclat est celui du jaune. [C’est la couleur qui tient le milieu
entre toutes. Com. II.]
<l>
La ligne 3 devient yin -
Lorsque l’éclat du soleil est à son déclin, ce n’est plus la joie qu’il inspire,
mais la tristesse. (Litt. : on ne fait plus de musique au moyen des
instruments de terre, ni en chantant, mais c’est le gémissement d’un vieillard.
Le tout indique la déchéance et le chagrin qu’elle cause.) (Com.) [L’éclat du
soleil ne peut toujours durer. Com. II.]
<l>
La ligne 4 devient yin -
Note. Nous avons ici une collection d’expressions proverbiales dont plusieurs se
rapportent à la forme même du Koua. Celui-ci peut, en effet, à la rigueur, figurer une
poutre entamée en haut et en bas, donc très défectueuse.
<l>
La ligne 5 passe au yang -
Quand le prince se met en expédition, on verse des larmes et pousse des
