inférieurs, veut rester fidèle à la vertu, il devra être très prudent.
COMMENTAIRE I
Kwài est « décider, régler ». Le fort le fait pour le faible. Il faut en
cela, fermeté et douceur, résolution et concorde. « Appel sincère »
