18. La Dégénerescence, Kū : délibération, embarras, trouble, soucis.
TEXTE I
La délibération, chose éminemment utile, fera triompher des
difficultés. On doit délibérer trois jours avant d’agir ; puis trois jours
après (sur les conséquences).
