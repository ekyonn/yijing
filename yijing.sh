10. Marche, Li : Marcher sur, se conduire, agir, suivre un chemin.
TEXTE I
L’homme qui sait marcher sur la queue d’un tigre sans le faire crier
réussira.
COMMENTAIRE I. SYMBOLISME
Ciel au-dessus d’une eau stagnante. C’est la faiblesse, la douceur
soumise marchant sur la force ; la joie répondant à la force. C’est le
fort juste et modéré occupant la dignité souveraine ; exempt de faute,
il est glorieux et brillant. Le grand et sage distingue le haut et le bas et
donne satisfaction assurée aux désirs du peuple.
<l>
La ligne 1 devient yin -
Quand on se conduit avec droiture, on va sans commettre de faute. [Une
conduite droite et pure est seule approuvable. Com.]
<l>
La ligne 2 devient yin -
Quand on suit la voie de la morale, on marche sur un terrain égal et facile ;
l’homme paisible et retiré s’affermit heureusement. [Gardant le milieu, il ne
tombe pas dans le désordre. Com. II.]
<l>
La ligne 3 devient yang -
L’homme privé d’un œil peut encore voir ; boiteux, il peut encore marcher,
quoique mal. Si dans cet état d’imperfection corporelle, on marche sur la
queue d’un tigre (on fait un faux pas), on sera mordu, il en arrivera malheur.
L’homme de guerre même peut faire un grand prince, s’il suit la bonne voie.
[Il réussira si sa volonté est énergique. Com. II.]
<l>
La ligne 4 devient yin -
Si même on est exposé à marcher sur la queue d’un tigre (à de grandes
difficultés) et que l’on agisse avec une crainte prudente, l’issue finale de
l’entreprise sera heureuse. [On atteindra le but. Com. II.]
<l>
La ligne 5 devient yin -
Si l’on marche avec fermeté, le progrès sera fort et solide (sera poussé en
avant). [Si la position est gardée convenablement. Com. II.]
<l>
La ligne 6 devient yin -
Si l’on a les yeux ouverts sur sa propre conduite et que l’on fasse attention
aux pronostics, tout l’ensemble des actes sera spécialement heureux. —
Quant aux chefs, il y aura lieu de les louer hautement.
