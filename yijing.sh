36. Obscurcissement de la lumière, Mīng ī : 1. Lumière blessée, faiblissante, entrant sous la terre ; 2.
Lumière sortant de l’orient, bourgeonner, ī.
TEXTE I
Mīng-ī indique un progrès dont l’affermissement est difficile. (Termes
divinatoires interpolés.) Profiter des difficultés pour se parfaire, être
droit et juste.
SYMBOLISME
Le soleil entre sous la terre (la nuit). Le sage, gouvernant les hommes,
de l’obscurité même sait encore briller.
COMMENTAIRE I
Mīng-ī est la lumière entrant sous la terre, brillante et docile.
Cruellement traité (ī) et dans de grandes difficultés, c’est ce que fut
Wen-Wang. — Dans de grandes difficultés et restant droit et ferme en
son cœur, c’est ce que fut Khi-tze.
<l>
La ligne 1 devient yin -
La lumière faiblissante (le jour finissant) dans son vol abaisse ses ailes
(descend sous terre). Le sage dans sa route sait jeûner plusieurs jours (plutôt
que d’abandonner la voie droite) ; où qu’il aille, les grands parlent de lui.
<l>
La ligne 2 passe au yang -
La lumière (dans sa route) est attaquée du côté gauche et repousse
l’assaut avec la force et la rapidité du cheval. (Ou bien : elle se répand à
gauche avec la force et la rapidité d’un cheval qui sauve son cavalier.)
<l>
La ligne 3 devient yin -
Elle va dans le sud et y fait prisonnier le grand chef (des ténèbres). Elle ne
peut souffrir aucun mal.
<l>
La ligne 4 passe au yang -
En allant du côté gauche, on s’associe à la pensée du principe lumineux,
lorsqu’il sort de son palais (pour se répandre sur le monde).
<l>
La ligne 5 passe au yang -
Khi-tze s’avance, la lumière l’emporte et triomphe définitivement. Ou
bien : l’éclat répandu par Khi-tze est bienfaisant et assuré.
<l>
La ligne 6 passe au yang -
Lorsque la lumière ne brille pas encore et que les ténèbres règnent, la
lumière s’élève d’abord dans le ciel, se répand, puis s’incline vers la terre.
[Elle éclaire les quatre plages et entre en terre perdant sa mesure. Marche du
soleil et du jour. Com. II.]
