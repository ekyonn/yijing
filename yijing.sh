40. Libération, Kieh : 1. Délivrer, faire échapper, échapper au danger ; 2. Disperser ;
3. Ouvrir, séparer, s’ouvrir. Se dit du mouvement de la germination.
4. Résoudre une difficulté, une complication.
TEXTE ET COMMENTAIRE I
Si l’on réussit à faire échapper aux dangers, on gagnera les gens à soi
et l’on aura des relations heureuses ; on gardera le milieu. En tout ce
que l’on fait, l’activité est chose utile et fait acquérir des mérites. Kieh
est : se trouvant en danger, savoir agir et échapper. Quand le ciel et la
terre ouvrent les pores (3 e sens) des êtres, le tonnerre et la pluie se
produisent. Alors les plantes et les arbres à fruit bourgeonnent. Bien
important est le temps où tout s’ouvre.
SYMBOLISME
Le tonnerre et la pluie forment le Koua Kieh. Le sage est indulgent
pour l’erreur et traite les coupables avec douceur.
<l>
La ligne 1 passe au yang -
Délivrer quelqu’un est chose excellente. [Quand le fort et le faible
s’entendent selon la justice, il n’y a pas de faute à craindre. Com. II.]
<l>
La ligne 2 devient yin -
Délivrer est aussi heureux que, pour le chasseur, prendre trois renards et
obtenir le prix, la flèche d’or. — Celui qui résout une difficulté, fait sortir d’une
position difficile, est comme le chasseur qui prend trois renards et obtient la
flèche d’or.
<l>
La ligne 3 devient yang -
Si un porteur se met dans un char et qu’il survienne des voleurs, il sera
attaqué et échappera difficilement ; s’il abandonne sa charge, il pourra se
sauver. (Com.) [Pour un porteur, aller en char est honteux ; c’est attirer sur
soi les voleurs. Com. II.] Il n’appartient pas à des gens du commun d’aller en
char.
<l>
La ligne 4 devient yin -
Échappez d’abord et après cela les amis viendront témoigner leur fidélité
