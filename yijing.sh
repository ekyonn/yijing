52. Immobilité,  Kán : ferme, tenir droit, bien réglé, arrêter, reposer.
TEXTE I
L’homme ferme s’oppose résolument (au mal) sans tenir compte de
lui-même. Devant traverser un endroit, il ne regarde pas qui s’y trouve
(mais le fait résolument) et ne faillit point.
SYMBOLISME
Deux montagnes superposées forment le  Koua.
Ainsi l’homme supérieur pense à ne pas dépasser les bornes de ses fonctions.
COMMENTAIRE
Kán signifie s’arrêter, tenir ferme, en bon ordre, agir ou s’arrêter selon
l’occasion. Quand l’acte et sa cessation ont lieu en temps convenable,
la conduite est belle et intelligente. « Rester à sa place », cela veut
dire que les grands et les petits sont en rapport, mais sans usurpation
ni entre-croisement. Celui qui est ferme et attentif à son devoir ne se
recherche pas lui-même. Marchant dans son jardin, il ne voit pas
même qui s’y trouve.
Note. Les traits illustrent le sens « tenir droit, bien réglé » et se réfère aux rites du
maintien extérieur qui prescrivent de se tenir toujours droit et de ne pas même
s’asseoir sur un siège qui n’est pas droit.
<l>
La ligne 1 passe au yang -
(On doit) tenir fermes ses pieds ; i. e. être toujours d’une ferme droiture.
[Ne jamais perdre sa droiture. Com. II.]
<l>
La ligne 2 passe au yang -
« Arrêter le mouvement des jambes ». (Ou les tenir droites et bien
posées.) Si, devant s’arrêter, on ne peut aider celui qui marche devant soi, on
doit en avoir du déplaisir. [On doit aider et se rendre au cri d’appel. Com. II.]
<l>
La ligne 3 devient yin -
Se tenir ferme à sa place et le corps (litt. : les côtes) bien tenu, (c’est ainsi
que l’on doit être).
<l>
La ligne 4 passe au yang -
Tenir le corps droit et ferme, c’est une bonne manière. [On doit tenir le
corps entier tout droit. Com. II.] (Répétition du § 3 pour en avoir six. Tenir le
corps droit est un principe essentiel des rites chinois.)
<l>
La ligne 5 passe au yang -
Tenir ses mâchoires de sorte que les paroles sortent de la bouche avec
ordre et mesure, cela exempte de repentir. [On sait ainsi conserver le milieu.
Com. II.]
<l>
La ligne 6 devient yin -
Être tout appliqué à rester ferme dans le bien est une disposition très
heureuse. [On peut ainsi être juste et droit jusqu’à la fin. Com. II.]
<h>
53. Progrès graduel, Tsién : avancement, progrès, graduellement, aller pas à pas ; se
marier, retourner chez ses parents pour les visiter (dit d’une femme).
TEXTE I
Se marier, pour une femme, est chose heureuse.
SYMBOLISME
Bois sur une montagne figure s’élever (avancer), se tenir droit. Le
sage se maintient en sainteté et améliore les mœurs du peuple.
COMMENTAIRE I
« Avancer » est aussi obtenir une dignité, acquérir des mérites. Si l’on
agit avec justice et droiture, on pourra gouverner parfaitement son
État et y faire régner la justice. — La dignité du grand exige qu’il garde
le milieu. — Si l’on se tient ferme en sa vertu et se montre doux,
docile, on n’épuisera pas ses forces et son action. (Moyen de conserver
son avancement, sa grandeur.)
<l>
La ligne 1 passe au yang -
Les oies sauvages vont pas à pas s’approchant de la rive. — Un jeune
homme circonspect n’aura point à se repentir, quoi qu’on dise.
<l>
La ligne 2 passe au yang -
Ces oies vont pas à pas vers des rochers où elles auront à manger et à
boire ; joyeuses et contentes (de leur prudence persévérante), elles y sont
tout à l’aise [et n’y ont pas une satisfaction vaine. Com. II].
<l>
La ligne 3 devient yin -
Ces oies vont pas à pas vers un monticule sur la terre sèche (elles n’y
trouvent rien). Ainsi un époux part pour une expédition dont il ne revient pas.
[Il quitte pour toujours sa compagne. Com. II.] Ainsi une épouse a conçu un
enfant qu’elle ne pourra élever (faute de soutien de son époux). Il faut arrêter
les brigands, causes de ses malheurs. [On doit se protéger l’un l’autre. Com.
II.]
<l>
La ligne 4 passe au yang -
Les oies avancent pas à pas vers un arbre. Qui gagnera les grosses
branches y sera bien, pourra s’y reposer.
<l>
La ligne 5 devient yin -
Elles s’avancent vers une hauteur qu’elles atteindront difficilement et
tardivement. [Ainsi une épouse est trois ans sans enfant, mais à la fin plus
d’empêchement ; elle obtient l’objet de ses désirs. Com. II.] (Il faut être
patient et ne point désespérer ; patience et longueur de temps font plus que
force.)
<l>
La ligne 6 devient yin -
Elles avancent pas à pas vers une hauteur ; quand elles y seront, elles
pourront donner à leurs ailes toute leur beauté en les déployant. [Plus rien ne
trouble quand on a atteint son but. Com. II.] (Patience et succès.)
<h>
54. La jeune fille, Kvêi-Mei : marier une jeune sœur, une jeune fille.
TEXTE I
On doit corriger le mal ou l’on n’aura aucun avantage. (Phrase mutilée.
Voir II, § I.)
SYMBOLISME
Le tonnerre au-dessus d’un marais forme le Koua. L’homme supérieur,
pour s’assurer une fin heureuse, pense au malheur.
COMMENTAIRE I
Marier une jeune sœur est un devoir suprême prescrit par le ciel et la
terre (à ses frères). Quand le ciel et la terre sont sans relation, les
êtres ne naissent pas. Marier une jeune fille est le principe et la fin de
l’homme ; c’est un acte qui cause la joie.
<l>
La ligne 1 devient yin -
Marier une sœur cadette en la donnant comme femme secondaire (c’est la
mettre dans la position d’un boiteux qui marche encore, mais mal). Ainsi
cette jeune fille est épouse, mais dans une condition inférieure. Corriger cet
abus sera chose excellente. [Cela se fait constamment. Com.]
<l>
La ligne 2 devient yin -
Elle est comme un borgne qui voit encore, mais mal (qui ne voit d’un œil).
[Il lui sera avantageux de vivre retirée et ferme en sa vertu, de ne point faillir
aux règles du devoir. Com. II.]
<l>
La ligne 3 devient yang -
La jeune fille mariée par devoir de soumission est souvent donnée comme
femme secondaire. [Cette soumission n’est pas bonne. Com. II.]
<l>
La ligne 4 devient yin -
La jeune fille à marier cherche à retarder le moment. [Modeste, elle n’a pas
hâte de suivre un homme. Com. II.] Mais bien qu’elle retarde son mariage, le
moment viendra malgré tout. [Malgré tout, cela se fera. Com. II.]
<l>
La ligne 5 passe au yang -
Quand l’empereur Ti-Y maria sa sœur, les manches (le vêtement) de la
mariée étaient moins richement ornées que celles de sa plus jeune sœur.
Note. Voir Koua II, texte II, § 5. La mariée avait perdu sa qualité de princesse royale.
Elle était comme la lune presque pleine (quant à sa vertu) de modestie, de
soumission. C’était de très heureux augure. Telle fut sa noble conduite. [Ainsi
elle maintint sa dignité dans la droiture. Com. II.]
<l>
La ligne 6 passe au yang -
Qu’une femme reçoive une corbeille vide de fruits (entièrement vide,
Com.), ou qu’un homme acquière une brebis qui n’a plus de sang, ils n’en
retirent pas d’avantage.
<h>
55. Abondance, Fâng : 1. Abondance, richesse, grand nombre d’amis ; 2. Avoir en
abondance, multiplier ; 3. Grandeur, élévation.
TEXTE I
Le prince qui possède la grandeur, l’abondance, doit être sans désir
inquiet ; il est comme le soleil à midi.
SYMBOLISME
Tonnerre et éclair forment le Koua fâng. Le grand et sage tranche les
discussions, décide des cas litigieux et fait exécuter les châtiments.
COMMENTAIRE I
Fâng est grandeur, prospérité. L’activité intelligente est cause de la
grandeur et de la prospérité. Le prince qui l’a obtenue est grand et
brillant ; il doit répandre son éclat sur le monde, l’éclairer. La lune
devient pleine, puis diminue. Le ciel et la terre sont alternativement
pleins et vides, croissent et diminuent avec les saisons. A plus forte
raison en est-il de l’homme et des esprits.
<l>
La ligne 1 devient yin -
Dans la rencontre des amis, l’égalité de rang est une source de
satisfaction. Aller en compagnie est chose excellente. [Si l’on veut surpasser
ses égaux, on s’attirera le malheur.]
<l>
La ligne 2 passe au yang -
En multipliant (fâng, 2 e sens) les pavillons, les tentures (autour de sa
tente), on produit une obscurité telle qu’on peut voir la constellation du
Sagittaire en plein jour. Si par là on s’est attiré les soupçons, la malveillance,
que l’on fasse paraître sa droiture par sa sincérité et l’on s’en trouvera bien.
Qu’on énonce ses pensées, ses fins d’une manière qui attire la confiance.
<l>
La ligne 3 devient yin -
En multipliant ses tentures, on peut (grâce à l’obscurité) voir l’étoile Mei
(au Sagittaire) en plein jour, mais alors on se cassera le bras droit (il arrivera
malheur).
<l>
La ligne 4 devient yin -
On peut ainsi voir, en plein jour, la constellation du Sagittaire ; ce sera une
occurrence heureuse si on trouve alors un ami.
<l>
La ligne 5 passe au yang -
Promouvoir (les talents, les mérites ou les arts, les créations belles et
précieuses) est un sujet de joie et de louange.
<l>
La ligne 6 passe au yang -
Élever haut sa maison (Com. : s’élever orgueilleusement vers le ciel),
l’entourer d’abris protecteurs, puis se tenir à la porte, tranquille, silencieux et
solitaire, rester ainsi longtemps (trois ans) sans recevoir personne, sans
parler à personne, ce n’est pas bien. (On sera isolé, abandonné et sans
secours au temps du danger.) — Ces actes prouvent l’orgueil qui veut s’élever
au ciel et la volonté de s’isoler des autres hommes, de manquer aux lois de la
bonté, de la concorde, etc.
<h>
56. Le vagabond, Lu : voyageur, hôte, étranger, loger ; bon arrangement.
TEXTE I
L’étranger ambulant prospère difficilement. S’il est juste et droit, il
aura une heureuse fortune.
COMMENTAIRE I
Si le faible acquiert, maintient sa droiture chez les étrangers et reste
soumis au fort, il sera stable et attaché à la claire vérité. Ainsi
l’étranger nomade, faible au commencement, prospérera et s’affermira
s’il est droit et juste.
SYMBOLISME
Le feu au-dessus d’une montagne. Ainsi le grand et sage fait briller sa
droiture en appliquant les lois pénales et ne laisse pas durer les
différends et les procès.
<l>
La ligne 1 passe au yang -
Les étrangers ambulants, petits et faibles, sont exposés à bien des maux.
[Quand le but est bas, les calamités s’ensuivent. Com. II.]
<l>
La ligne 2 passe au yang -
L’étranger qui s’est établi quelque part doit y déposer tous ses biens et se
procurer des serviteurs jeunes et habiles ; il lui arrivera bonheur.
<l>
La ligne 3 devient yin -
Il arrive que cet étranger brûle sa maison, perde ses bons et intelligents
serviteurs, sa prospérité est bien en péril. [Il brûle sa maison en se ruinant ;
en fréquentant les gens vulgaires, il perd son habileté, sa rectitude. Com. II.]
<l>
La ligne 4 devient yin -
Il peut s’être établi quelque part, y avoir ses biens et sa hache, mais son
cœur n’est point encore à l’aise et en sûreté. (Il n’a point une situation
assurée, il doit veiller.)
<l>
La ligne 5 passe au yang -
Il tire un faisan et sa flèche s’échappe une fois (manque le but) ; à la fin,
par les louanges qu’il sait mériter, il reçoit accueil et fonction du prince.
<l>
La ligne 6 devient yin -
L’oiseau détruit son nid ; l’étranger ambulant rit d’abord, puis pousse de
hauts cris, parce qu’il perd son bœuf ; ainsi, trop aisément, un changement
amène un mal.
<h>
57. Pénétration douce, Sún : doux, condescendant ; choisir ; tenir fermement.
TEXTE I
Avec un faible développement même, par la douceur, la
condescendance, on pourra réussir dans ses entreprises et se montrer
véritablement grand.
SYMBOLISME
Deux fois le vent forme le Koua sûn. Ainsi le grand et sage sait réitérer
ses ordres pour mener à bonne fin ses affaires.
COMMENTAIRE I
On doit redoubler de bonté quand on réitère ses ordres. Si la fermeté
et la bonté restent dans le juste milieu, ce qu’on projette se fera. Le
faible doit céder au fort ; ainsi commence le progrès, ainsi l’on réussit
et se montre grand.
<l>
La ligne 1 passe au yang -
Pour avancer et reculer (à propos, pour agir avec fermeté ou
condescendance), il faut aussi la ferme droiture d’un homme d’armes (et pas
seulement la douceur). [Dans la perplexité, cette fermeté fera régler sa
volonté avec prudence. Com. II.]
<l>
La ligne 2 devient yin -
Quand la longanimité a été poussée trop loin (litt. : quand elle est en
dessous du lit. Com. : Kvoh yu sun. Quand la supériorité s’épuise. Com. II.
C’est-à-dire quand on s’abaisse trop), il sera bien de consulter les devins
quant à l’objet de la perplexité, et l’on ne commettra pas de faute. [Ceci
résultera de ce qu’on gardera le juste milieu. Com. II.]
<l>
La ligne 3 devient yin -
Une condescendance trop hâtive peut causer des regrets [la force de la
volonté s’émousse. Com. II] ou : une douceur constante sans rigueur.
<l>
La ligne 4 passe au yang -
Quand on est sans crainte ni regret, on va à la chasse prendre du gibier
pour le triple usage réglé. [i. e. offrandes de sacrifice, nourriture des hôtes,
entretien de la cuisine. Com.]. [On acquiert ainsi des mérites. Com. II.] Ceci
se rapporte à sún, mets (nourriture).
<l>
La ligne 5 devient yin -
Pour que la fortune soit heureuse, tout regret dissipé, que rien ne soit sans
avantage et qu’un commencement malheureux ait une fin excellente, il faut,
en tout changement, réfléchir trois jours avant et trois jours après. Alors tout
ira bien. (Ainsi faut-il délibérer sur les actes de condescendance et de
fermeté.)
<l>
La ligne 6 devient yin -
Quand la condescendance, la bonté vont trop loin, on perd ses biens et ses
moyens de défense (sa hache) et la prospérité tourne en désastre. [La
droiture en méchanceté. Com. II.]
<h>
58. Joie, Túi : satisfaction, réjouir (les autres).
TEXTE I
Túi est joie. Cela conduit à toute prospérité.
SYMBOLISME
Eau stagnante sur eau stagnante forme la figure. Le grand et sage
favorise l’amitié et encourage la pratique de la vertu.
COMMENTAIRE
Quand on donne la joie au monde, on obéit au ciel, on
seconde les désirs des hommes. Quand on cherche avant tout à faire
le bonheur du peuple, le peuple oublie ses maux ; excité par là à
entreprendre les choses difficiles, il oublie jusqu’aux dangers de mort.
Par cette satisfaction, le peuple est fortement animé (à remplir son
devoir).
<l>
La ligne 1 devient yin -
Établir la concorde, satisfaire tout le monde, est une source de prospérité.
Cela étouffe les défiances et les velléités de résistance. Com. II.
<l>
La ligne 2 devient yin -
La sincérité, la droiture, satisfait tout le monde et prévient les regrets [en
établissant la confiance dans les intentions. Com. II].
<l>
La ligne 3 devient yang -
Une satisfaction briguée, forcée, est chose mauvaise. [Le mal consiste en
ce que la confiance est donnée à un indigne. Com. II.]
<l>
La ligne 4 devient yin -
Se préoccuper de satisfaire de manière à perdre le calme, est proche de la
peine [quand on peut avoir la joie, la paix intérieure. Com. II]. Chercher à
rendre la joie à qui n’a pas la paix du cœur, c’est aider un malade à recouvrer
la santé.
<l>
La ligne 5 devient yin -
Se confier en ce qui est nuisible est chose bien dangereuse.
<l>
La ligne 6 passe au yang -
Faire arriver à un état de satisfaction, de joie (c’est le sens du mot en
question), est le but de cette section.
<h>
59. Dispersion, Hwán : extension, exposition, développement, abondance
débordante ; dispersion, dissipation
TEXTE I
« Abondance débordante ». Le prince qui fréquente le temple de ses
ancêtres traversera heureusement les difficultés ; il parviendra à une
prospérité ferme.
SYMBOLISME
Le vent soufflant au-dessus de l’eau forme le Koua de hwân, disperser.
Les anciens rois faisaient le sacrifice à Shang-Ti et élevaient des
temples ancestraux.
COMMENTAIRE I
La puissance lui viendra sans diminuer. — Fréquentant le temple des
ancêtres, il observera le milieu fixe. — Les hommes condescendants et
bons auront leurs places convenables au dehors et le grand fera
régner l’harmonie. (On les donnera convenablement.)
<l>
La ligne 1 passe au yang -
On doit remédier à cette dispersion, Com.], en délivrer un État. Si l’on use
de toute force (litt. : d’une force de cheval) pour cela, on réussira [à rétablir
l’ordre et l’obéissance. Com. II]. — Explication du second sens. — On doit
surtout user de sages enseignements et exhortations.
<l>
La ligne 2 devient yin -
Si, dans cet état dangereux, on se retire précipitamment dans l’obscurité,
on n’aura pas à s’en repentir [on arrivera au but de son désir, le
rétablissement de l’ordre et l’union. Com. II] ou : dans l’état de discorde, il
faut se retirer dans l’obscurité.
<l>
La ligne 3 devient yang -
Si l’on fait alors abstraction de soi-même [et s’occupe du dehors, Com. II]
on ne le regrettera pas.
<l>
La ligne 4 passe au yang -
Disperser la multitude, les associations, disperser heureusement ce qui est
uni et coalisé comme un monticule, c’est ce qu’un homme vulgaire ne saurait
même projeter.
Note. Le sens paraît être diviser, empêcher les coalitions mauvaises pour réunir dans
l’ordre et l’unité politique et sociale. Ou bien : disperser les petits et réunir les grands
pour le gouvernement. (Com.)
<l>
La ligne 5 devient yin -
Répandre (hwán) la sueur avec des cris (de crainte et souffrance) ;
répandre, distribuer (les biens accumulés dans) les magasins royaux, et cela
sans regret, pour remédier aux maux des troubles et de la misère qui en est
la conséquence.
Note. Ces deux phrases peuvent être indépendantes et exprimer simplement des
emplois divers du mot hwán ; ou bien on peut construire ainsi : Quand la peine et la
crainte pénètrent le peuple au point de lui faire pousser des cris comme s’il transpirait
fortement, il faut alors l’aider au moyen des provisions accumulées dans les magasins
publics.
<l>
