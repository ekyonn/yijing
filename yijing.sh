4. Folie juvénile, Meng : Intelligence non encore développée ; être non encore formé.
TEXTE I
Pour le développement (de l’intelligence) ce n’est point au maître (à
venir à nous), il n’en a pas besoin, mais au disciple inexpérimenté à
aller au maître. [Quand quelqu’un consulte le sort, le devin lui en dit
une première fois l’oracle ; si à une deuxième ou troisième fois, on
n’est pas écouté, on ne l’annonce plus, on n’enseigne plus. Interpolation.]
SYMBOLISME
Trigramme supérieur : montagne ; inférieur : précipice ; [fermeté
devant le danger. Com. I]
En haut : montagne ; en bas : source jaillissante. [Le sage agit avec
fermeté courageuse et développe la vertu. Com. II]
COMMENTAIRE I
L’ignorant progresse quand on le fait avancer en temps opportun ;
l’entretenir et rendre bon est l’office du sage.
<l>
La ligne 1 passe au yang -
Pour dissiper l’ignorance, il faut user des châtiments. [Pour maintenir les
règles en vigueur. Com. II.] Il faut user d’avertissements et de punitions pour
écarter toute cause de regret.
<l>
La ligne 2 devient yin -
Il est bien de se préoccuper de l’ignorant et de soutenir la jeune fille. Ainsi,
ils pourront triompher de leur ignorance. [L’enfant fait durer la famille ; il faut
en avoir soin. Com. II. Le fort et le faible doivent s’entraider.]
<l>
La ligne 3 devient yang -
N’épousez pas une fille si elle ne considère que l’or, si elle ne se possède
pas elle-même ; cela ne serait pas heureux. [Ce ne serait point agir
convenablement. Com. II.] C’est-à-dire une fille ayant la qualité dont il s’agit
n’est pas à épouser.
<l>
La ligne 4 passe au yang -
L’ignorance pauvre et abandonnée est chose funeste. [L’abandon,
l’isolement tient éloignée la possession de la vérité. Com. II.]
<l>
La ligne 5 passe au yang -
L’ignorant, tout jeune encore, peut arriver à bien (être instruit). [Le bien
de son état est la docilité, la douceur. Com. II.]
<l>
La ligne 6 devient yin -
Si l’on châtie l’ignorant en le formant, on ne doit point lui faire de tort,
mais au contraire l’en préserver.
