<l>
La ligne 6 devient yin -
Dans ses brillants exploits, il brise les têtes des chefs et reçoit à merci ceux
qui ne se sont point associés à leur révolte. Ainsi, il n’encourt aucun blâme.
[Ceci illustre la puissance, l’éclat des rois et des princes. Com.] [Ces
expéditions ont pour but de rétablir l’ordre et la justice. Com. II.]
