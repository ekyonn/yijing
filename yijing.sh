16. L'Enthousiasme, Yú : 1. Dignité, majesté ; 2. Aise, satisfaction, jouissance.
TEXTE I
La dignité fera établir avantageusement les chefs féodaux et conduire
les armées. (Un prince plein de dignité y réussira.)
SYMBOLISME
Le koua est formé par le tonnerre sortant de la terre avec fracas. Les
Anciens faisaient leur musique en conséquence et honoraient la vertu.
Offrant leurs adorations suprêmes à Shang-ti, ils faisaient des libations
à leurs ancêtres.
COMMENTAIRE I
La puissance forte, qui fait triompher convenablement ses volontés et
agit avec condescendance, a la vraie majesté. La condescendance est
semblable à celle du ciel et de la terre ; elle agit comme eux.
Comment ne pourrait-elle pas établir solidement ses vassaux et guider
ses troupes ? Le ciel et la terre agissent avec condescendance. Aussi le
soleil et la lune ne s’arrêtent point et les quatre saisons ne sont point
troublées dans leurs cours. L’homme grand et sage agit de même ;
aussi ses lois et châtiments sont justes et le peuple se soumet. C’est
une chose bien grande que la dignité noble.
<l>
La ligne 1 passe au yang -
Une dignité qui se manifeste bruyamment (ou bien : une jouissance
bruyante, 2 e sens) est chose funeste. [La volonté s’épuise. Com. II.]
<l>
La ligne 2 passe au yang -
La jouissance qui s’appuie sur le roc aura bientôt d’heureux succès.
<l>
La ligne 3 devient yang -
Si quelqu’un porte des yeux cupides sur les jouissances et s’y attarde, il
aura à s’en repentir.
<l>
La ligne 4 devient yin -
Par la dignité, on acquiert la grandeur, de grands biens, qu’on n’en doute
pas. Les amis viendront nombreux et pressés. [Les désirs s ’ accompliront
complètement. Com. II.]
<l>
La ligne 5 passe au yang -
Le plaisir est une maladie chronique qui se perpétue et grandit bien qu’on
n’en meure pas.
<l>
La ligne 6 passe au yang -
Si celui qui est livré aux jouissances se corrige complètement, il évitera les
maux qui en sont la suite. [S’il continue, cela ne pourra durer. Com. II.]
