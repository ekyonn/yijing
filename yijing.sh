19. La Surveillance, Lin : autorité, surintendance, fonction.
TEXTE I
L’exercice de l’autorité ne sera pas huit mois sans désagrément.
SYMBOLISME
La terre au-dessus des eaux stagnantes. Le sage instruit sans se
relâcher ; il forme et protège le peuple sans y mettre de bornes.
COMMENTAIRE I
L’autorité ferme grandit peu à peu. Bonne et complaisante, elle
gardera le milieu et observera ses devoirs. Par la ferme droiture on
obtiendra grand succès ; c’est la voie du ciel. Au bout de huit mois, il
pourra survenir quelque mal ; mais il ne durera pas (si l’on est sage).
<l>
La ligne 1 devient yin -
L’autorité procédant avec sentiments de concorde est une cause de succès
certain. [Elle atteindra son but sûrement. Com. II.]
<l>
La ligne 2 devient yin -
Si elle agit ainsi, les avantages en résulteront certainement.
<l>
La ligne 3 devient yang -
Se plaire au pouvoir pour lui-même est sans autre avantage et agrément.
Si on rejette ce vain sentiment, on exercera l’autorité heureusement.
<l>
La ligne 4 passe au yang -
La suprême autorité doit être sans défaillance [exercée convenablement.
Com. II.]
<l>
La ligne 5 passe au yang -
Savoir bien exercer l’autorité est nécessaire au grand prince et chose très
heureuse. [Le prince doit suivre la voie du milieu. Com. II.]
<l>
La ligne 6 passe au yang -
L’autorité généreuse et sincère est une source de bonheur sans ombre
(sans cause de regret). [Ces dispositions doivent se maintenir dans le cœur.
Com. II.]
<h>
20. Contemplation, Kwèn : 1. Regarder, contempler, regard ; 2. Apparence extérieure,
maintien ; 3. Gravité, dignité.
TEXTE I
Maintien, gravité comme celle de quelqu’un qui est purifié et prêt à
